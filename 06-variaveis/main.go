package main

import "fmt"

func main() {
	a := 10   // tipagem automatica
	var b int // var com tipo explicito
	b = 10
	var c int = 10 // var com tipo explicito e vlor declarado

	var d, e int = 10, 10 // multiplas declarações

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
}
